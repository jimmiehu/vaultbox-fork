import esbuild from "esbuild";
import { builtinModules } from "node:module";
import process from "node:process";

const prod = process.argv[2] === "production";
const watch = process.argv.includes("--watch");

const banner =
  "/* THIS IS A GENERATED/BUNDLED FILE BY ESBUILD. Edit the source in src/. */";
const builtins = [...builtinModules, ...builtinModules.map((name) => `node:${name}`)];

const context = await esbuild.context({
  banner: {
    js: banner,
  },
  bundle: true,
  entryPoints: ["src/main.ts"],
  external: [
    "obsidian",
    "electron",
    "@codemirror/autocomplete",
    "@codemirror/collab",
    "@codemirror/commands",
    "@codemirror/language",
    "@codemirror/lint",
    "@codemirror/search",
    "@codemirror/state",
    "@codemirror/view",
    "@lezer/common",
    "@lezer/highlight",
    "@lezer/lr",
    ...builtins,
  ],
  format: "cjs",
  logLevel: "info",
  minify: prod,
  outfile: "main.js",
  platform: "browser",
  sourcemap: prod ? false : "inline",
  target: "es2021",
  treeShaking: true,
});

if (watch) {
  await context.watch();
  console.log("Vaultbox build watcher started.");
} else {
  await context.rebuild();
  await context.dispose();
}
